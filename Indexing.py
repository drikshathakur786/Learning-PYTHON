# Slicing, Indexing
name="Driksha Thakur"
print(name[-2])
print(name[5])

print(name[0:7])

print(name[-6:])

print(name[:])

# name[start:end:step]

# step size- characters u want to skip. By default stepsize is 1
print(name[0:14:1])

#selecting every second character
print(name[0:14:2])

print(name[::2])

print(name[::-1])

