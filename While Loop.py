# Loops: repeat a code several types
# 2 types- for loop (when start and end is known) and while loop(when start known but end unknown/condition)

# print name 10 times
count= 1
while count<=10:
    print("Driksha")
    count= count+1

# print counting before name till 10
count= 1
while count<=10:
    print(count,"Driksha")
    count= count+1

# input any number from user and print their table  eg: 4 X 1 = 4 till 10
n= 4
count= 1

while count<=10:
    print(n,"X",count,"=",n*count)
    count = count + 1

